vec3 getDirectionOffset(int direction)
{
    if (direction == DIRECTION_NORTH)
        return { 0, -1, 0 };
    else if (direction == DIRECTION_EAST)
        return { 1, 0, 0 };
    else if (direction == DIRECTION_SOUTH)
        return { 0, 1, 0 };
    else // WEST
        return { -1, 0, 0 };
}